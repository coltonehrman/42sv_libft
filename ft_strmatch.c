#include "libft.h"

int		ft_strmatch(char *s1, char *s2)
{
	while (*s1 && *s2)
	{
		if (*s1 != *s2)
			return (0);
		s1++;
		s2++;
	}
	return ((*s2) ? 0 : 1);
}
